package data;

public class TwitterUserObject {
	private String twitter_handle;
	private String twitter_name;
	private int avg_total_engagement;
	private int avg_at_20;
	public String getTwitter_handle() {
		return twitter_handle;
	}
	public void setTwitter_handle(String twitter_handle) {
		this.twitter_handle = twitter_handle;
	}
	public String getTwitter_name() {
		return twitter_name;
	}
	public void setTwitter_name(String twitter_name) {
		this.twitter_name = twitter_name;
	}
	public int getAvg_total_engagement() {
		return avg_total_engagement;
	}
	public void setAvg_total_engagement(int avg_total_engagement) {
		this.avg_total_engagement = avg_total_engagement;
	}
	public int getAvg_at_20() {
		return avg_at_20;
	}
	public void setAvg_at_20(int avg_at_20) {
		this.avg_at_20 = avg_at_20;
	}
	
	
}
